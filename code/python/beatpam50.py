def mean_sd(x):
    x = np.asarray(x, dtype=float)
    return float(x.mean()), float(x.std(ddof=0))

def softmax(S):
    S = S - S.max(axis=1, keepdims=True)
    ex = np.exp(S)
    return ex / (ex.sum(axis=1, keepdims=True) + 1e-12)

